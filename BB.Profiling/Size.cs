
namespace BB.Profiling
{
	/// <summary>
	/// Holds pre-computed sizes for primitive objects.
	/// </summary>
	public class Size
	{
		public const long Boolean = sizeof(System.Boolean);

		public const long Byte = sizeof(System.Byte);

		public const long Char = sizeof(System.Char);

		public const long Decimal = sizeof(System.Decimal);

		public const long Double = sizeof(System.Double);

		public const long Int16 = sizeof(System.Int16);

		public const long Int32 = sizeof(System.Int32);

		public const long Int64 = sizeof(System.Int64);

		public const long SByte = sizeof(System.SByte);

		public const long Single = sizeof(System.Single);

		public const long UInt16 = sizeof(System.UInt16);

		public const long UInt32 = sizeof(System.UInt32);

		public const long UInt64 = sizeof(System.UInt64);
	}
}
